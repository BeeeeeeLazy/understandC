/*女朋友数量显示*/
#include<stdio.h>
int main(void)
{
	int girlfriendsNumber;
	int girlfriendsNumberAfterBreakUp;

	girlfriendsNumber = 1;
	girlfriendsNumberAfterBreakUp = girlfriendsNumber - 1;
	printf("Your girlfriends number is %d.\nYour girlfriends number after break up was %d.\n",
		girlfriendsNumber, girlfriendsNumberAfterBreakUp);
	return 0;
}